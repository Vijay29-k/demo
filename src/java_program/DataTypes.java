package java_program;

public class DataTypes {

	public static void main(String[] args) {
		
		byte a = 10; //1byte, 8 bit, 0 
		
		System.out.println(a);
		
		short b = 32760; //2 byte, 16 bit, 0 
		
		int i = 3000000; //4byte, 32 bit, 0
		
		long l1 = 424344343435L; //8byte, 64 bit, 0
		
		double d = 0; //8byte, 64bit, 0.0
		
		System.out.println(d);
		
		float f1 = 123.445f; //4byte, 32 bit, 0.0
		
		char ch = 'c' ; //2byte, 16bit
		
		System.out.println(ch);
		
		boolean res = false ;

		
		System.out.println(res);
		
		
	}

}
